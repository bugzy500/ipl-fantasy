const FantasyTeam = require('../models/FantasyTeam.model');
const { getActiveLeagueMemberIds } = require('../services/league-members.service');

// GET /api/leaderboard/match/:matchId
// Ranks all users by totalPoints for that specific match.
const getMatchLeaderboard = async (req, res) => {
  try {
    const activeMemberIds = await getActiveLeagueMemberIds();
    if (activeMemberIds.length === 0) return res.json([]);

    const teams = await FantasyTeam.find({ matchId: req.params.matchId, userId: { $in: activeMemberIds } })
      .populate('userId', 'name')
      .sort({ totalPoints: -1 });

    const leaderboard = teams
      .filter((t) => t.userId != null)
      .map((t, idx) => ({
        rank: idx + 1,
        userId: t.userId._id,
        userName: t.userId.name,
        totalPoints: t.totalPoints,
        teamId: t._id,
        isAutoGenerated: t.isAutoGenerated || false,
      }));

    res.json(leaderboard);
  } catch (err) {
    res.status(500).json({ message: err.message });
  }
};

// GET /api/leaderboard/season
// Aggregates points across all completed matches (not abandoned).
const getSeasonLeaderboard = async (req, res) => {
  try {
    const activeMemberIds = await getActiveLeagueMemberIds();
    if (activeMemberIds.length === 0) return res.json([]);

    const aggregate = await FantasyTeam.aggregate([
      { $match: { userId: { $in: activeMemberIds } } },
      // Only count teams from completed matches
      {
        $lookup: {
          from: 'matches',
          localField: 'matchId',
          foreignField: '_id',
          as: 'match',
        },
      },
      { $unwind: '$match' },
      { $match: { 'match.status': 'completed' } },
      {
        $group: {
          _id: '$userId',
          totalPoints: { $sum: '$totalPoints' },
          matchesPlayed: { $sum: 1 },
        },
      },
      { $sort: { totalPoints: -1 } },
      {
        $lookup: {
          from: 'users',
          localField: '_id',
          foreignField: '_id',
          as: 'user',
        },
      },
      { $unwind: '$user' },
      {
        $project: {
          _id: 0,
          userId: '$_id',
          userName: '$user.name',
          totalPoints: 1,
          matchesPlayed: 1,
        },
      },
    ]);

    const leaderboard = aggregate.map((entry, idx) => ({ rank: idx + 1, ...entry }));
    res.json(leaderboard);
  } catch (err) {
    res.status(500).json({ message: err.message });
  }
};

const PlayerPerformance = require('../models/PlayerPerformance.model');

// GET /api/leaderboard/breakdown/:userId
const getUserBreakdown = async (req, res) => {
  try {
    const { userId } = req.params;
    
    // 1. Fetch all teams for user, returning populated match and players
    const teams = await FantasyTeam.find({ userId })
      .populate('matchId', 'team1 team2 scheduledAt status result')
      .populate('players', 'name franchise role imageUrl')
      .sort({ createdAt: -1 })
      .lean();

    // 2. Extract match IDs to fetch performances
    const matchIds = teams.map(t => t.matchId._id);
    const performances = await PlayerPerformance.find({ matchId: { $in: matchIds } }).lean();

    // 3. Group by matchId -> playerId for quick lookup
    const perfMap = {};
    for (const p of performances) {
      if (!perfMap[p.matchId]) perfMap[p.matchId] = {};
      perfMap[p.matchId][p.playerId] = p;
    }

    // 4. Build output
    const breakdown = teams.map(team => {
      const matchPerfMap = perfMap[team.matchId._id] || {};
      
      const populatedPlayers = team.players.map(player => {
        return {
          player,
          performance: matchPerfMap[player._id] || null,
          isCaptain: String(team.captain) === String(player._id),
          isViceCaptain: String(team.viceCaptain) === String(player._id),
        };
      });

      return {
        teamId: team._id,
        match: team.matchId,
        totalPoints: team.totalPoints,
        players: populatedPlayers
      };
    });

    res.json(breakdown);
  } catch (err) {
    res.status(500).json({ message: err.message });
  }
};

module.exports = { getMatchLeaderboard, getSeasonLeaderboard, getUserBreakdown };
